def uniqueEmail(emails):
    stack = set()
    for email in emails:
        l_name,d_name = email.split('@')
        l_name = l_name.split('+')[0].replace('.','')
        # l_name = l_name.replace('.','')
        # em = l_name + d_name
        stack.add(l_name+d_name)
    return len(stack)

